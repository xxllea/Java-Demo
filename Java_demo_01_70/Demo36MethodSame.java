/* 
题目要求：
定义一个方法，用来判断两个数字是否相同。
*/
public class Demo36MethodSame {
    public static void main(String[] args) {
        System.out.println(isSame(3, 6));
    }

    /* 
    三要素：
    返回值类型：boolean
    方法名称：isSame
    参数列表：
    */
    public static boolean isSame(int a, int b) {
        if (a == b) {
            return true;
        } 
        return false;
    } 
}